public class Customer {
    
    int congHaiSo(int a, int b){
        return a + b ;
    }
    int hieuHaiSo(int a, int b){
        return a - b ;
    }
    int tichHaiSo(int a, int b){
        return a * b ;
    }
    double thuongHaiSo(double a, double b){
        return a / b ;
    }
}

